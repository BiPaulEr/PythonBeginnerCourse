"""
This module contains mathematical operations, including the function to 
add two numbers.

Functions:
- add_numbers: Adds two numbers (either integers or floats) and returns the sum.
"""
def add_numbers(a, b):
    """
    Add two numbers and return the result.
    
    :param a: The first number.
    :type a: int or float
    :param b: The second number.
    :type b: int or float
    :return: The sum of a and b.
    :rtype: int or float
    """
    return a + b
